      .h4 1.5 Modelos de Gobierno electrónico   
    .p El gobierno electrónico puede definirse como el uso de las TIC para brindar servicios gubernamentales de manera más efectiva y eficiente a ciudadanos y empresas. Es la aplicación de las TIC en las operaciones gubernamentales, logrando fines públicos por medios digitales. El objetivo es mejorar el funcionamiento interno del sector público mediante la reducción de los costos financieros y los tiempos de transacción para integrar mejor los flujos y procesos de trabajo, con eficencia, buscando soluciones sostenibles. El principio debe respaldarse por un marco institucional de gobierno electrónico eficaz, a través de la innovación, transparencia, inclusión y rendición de cuentas a los ciudadanos y así restaurar la confianza de los ciudadanos en sus gobiernos.         

