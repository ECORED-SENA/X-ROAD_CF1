      .h4 1.3 Paradigmas que influenciaron la creación del Gobierno electrónico
    .p El conjunto de los elementos de la lección anterior se tradujeron en dos grandes enfoques o paradigmas que influenciaron la administración pública en las décadas recientes: el neo-institucionalismo y la Nueva Gestión Pública (NGP).

