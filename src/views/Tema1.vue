            .p Según esta corriente el cambio es resultado de la interacción entre institución y entorno. En tanto el ritmo de las transformaciones en el entorno es mucho más rápido que las transformaciones al interior de las instituciones, se produce una constante tensión entre ambos que obliga a un continuo proceso de adaptación.
            
